// Export a function to get a subset of events
export function getRandomCuratedEvents(count: number, seed: number): HistoricalEvent[] {
  // Simple seeded random function
  const seededRandom = (max: number, seed: number) => {
    const x = Math.sin(seed) * 10000;
    return Math.floor((x - Math.floor(x)) * max);
  };
  
  // Create a copy of the events array
  const events = [...allCuratedEvents];
  const result: HistoricalEvent[] = [];
  
  // Select 'count' random events
  for (let i = 0; i < count && events.length > 0; i++) {
    const index = seededRandom(events.length, seed + i);
    result.push(events[index]);
    events.splice(index, 1);
  }
  
  return result;
}
